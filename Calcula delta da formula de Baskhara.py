# Calcula delta da formula de Baskhara
a = float(input("Digite o valor correspondente ao numero que acompanha x quadrado: "))
b = float(input("Digite o valor correspondente ao numero que acompanha x: "))
c= float(input("A variavel independente: "))
# Delta da Equacao Quadratica
delta = b**2-4*a*c
# Imprime os resultados
print( "delta", delta)